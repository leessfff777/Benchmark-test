package org.owasp.benchmark.custom;

import java.sql.*;

public class customUtils {

    /**
     * 封装 statement.execute(sql, Statement.RETURN_GENERATED_KEYS)
     * @param conn 数据库连接（调用方传入，需保证有效）
     * @param sql 要执行的SQL语句
     * @return boolean 同原生execute返回值：true=返回ResultSet，false=返回更新计数
     * @throws SQLException 抛出原生JDBC异常（保持与原生方法一致）
     */
    public static boolean executeUtils(Connection conn, String sql) throws SQLException {
        // 创建Statement并执行核心方法
        Statement stmt = conn.createStatement();
        return stmt.execute(sql, Statement.RETURN_GENERATED_KEYS);
    }

    /**
     * 重载：支持手动传入flag（兼容原生所有execute重载场景）
     * @param conn 数据库连接
     * @param sql 要执行的SQL
     * @param autoGeneratedKeys 执行标识（如Statement.RETURN_GENERATED_KEYS/NO_GENERATED_KEYS）
     * @return boolean 原生execute返回值
     * @throws SQLException 原生JDBC异常
     */
    public static boolean executeUtils(Connection conn, String sql, int autoGeneratedKeys) throws SQLException {
        Statement stmt = conn.createStatement();
        return stmt.execute(sql, autoGeneratedKeys);
    }
}